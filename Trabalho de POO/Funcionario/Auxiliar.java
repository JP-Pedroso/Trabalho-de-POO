package Funcionario;

public class Auxiliar {

    private float t1;
    private float t2;
    private float d1;
    private float d2;
    private float d3;
    private float s1;
    private float s2;

    public Auxiliar() {
        this.t1 = 1.1f;
        this.t2 = 1.2f;
        this.d1 = 1.05f;
        this.d2 = 1.1f;
        this.d3 = 1.2f;
        this.s1 = 1.05f;
        this.s2 = 1.1f;
    }

    public float getT1() {
        return t1;
    }

    public float getT2() {
        return t2;
    }

    public float getD1() {
        return d1;
    }

    public float getD2() {
        return d2;
    }

    public float getD3() {
        return d3;
    }

    public float getS1() {
        return s1;
    }

    public float getS2() {
        return s2;
    }


    

}
